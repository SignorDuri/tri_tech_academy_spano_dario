CREATE TABLE SPANO_CANDIDATE(
ID NUMBER PRIMARY KEY,
FIRST_NAME VARCHAR2(50) NOT NULL,
LAST_NAME VARCHAR2(50) NOT NULL,
BIRTHDAY_DATE DATE NOT NULL,
EMAIL VARCHAR2(60) NOT NULL,
ADDRESS VARCHAR2(50) NOT NULL,
TELEPHONE VARCHAR2(10)NOT NULL
);

CREATE TABLE SPANO_JOBOFFER(
ID NUMBER PRIMARY KEY,
AGENCY VARCHAR2(50) NOT NULL,
STATUS VARCHAR2(50) NOT NULL,
JOB_TYPE VARCHAR2(50) NOT NULL,
CITY VARCHAR2(50) NOT NULL
);

CREATE TABLE SPANO_COMMUNICATION(
ID NUMBER PRIMARY KEY,
ID_CANDIDATE NUMBER REFERENCES SPANO_CANDIDATE(ID)ON DELETE CASCADE,
ID_JOBOFFER NUMBER REFERENCES SPANO_JOBOFFER(ID) ON DELETE CASCADE,
DESCRIPTION VARCHAR2(500),
DATA DATE
);

CREATE TABLE SPANO_JOBAPPLICATION(
ID NUMBER PRIMARY KEY,
ID_CANDIDATE NUMBER REFERENCES SPANO_CANDIDATE (ID) ON DELETE CASCADE,
DESCRIPTION VARCHAR2(250),
STATUS VARCHAR2(100),
CANDIDATURE_DATE DATE
);



